/**
 * Мусор-фильтр релевантности рекламного кластера товару.
 *
 * Задача: для нового кластера (pending) дать РЕКОМЕНДАЦИЮ — закинуть в работу ('approve')
 * или в чёрный список ('blacklist'). Это ADVISORY: движок только подписывает, решение
 * принимает человек руками. См. product-cluster-pending-review / decision.v2.
 *
 * Постановка (важно): downside нерелевантного кластера ограничен самим движком (бесзаказный
 * копит максимум до Макс СРО и сам уходит в excluded_high). Поэтому задача — НЕ точная
 * классификация, а ДЕШЁВЫЙ отсев явного мусора с уклоном «сомневаешься — пускай». Фильтр
 * асимметричен в сторону 'approve'; 'blacklist' даём только при явных признаках мусора.
 *
 * Сигналы (по убыванию силы):
 *  0. learnedNegative — у кластера есть слово, которое МЕНЕДЖЕР ранее уже бракнул в чёрный
 *     по этому товару (обучение от действий). Самый сильный сигнал: перебивает даже заказы —
 *     именно случай «для шиншилл» (заказы есть, но позиционирование чужое) → 'blacklist'.
 *  1. hasOrders — по фразам кластера уже идут заказы (РК или JAM-органика). Товар реально
 *     продаётся по этому запросу → запрос релевантен → 'approve'.
 *  2. matchedTokens — пересечение стем-токенов фраз кластера с «релевантным набором»
 *     (название/бренд/предмет товара + фразы уже одобренных кластеров + выученные positive).
 *     Есть пересечение → запрос про этот товар → 'approve'.
 *  3. Нет ни заказов, ни пересечения токенов → явный мусор → 'blacklist'.
 *
 * Серая зона (нет данных для суждения — у кластера не извлеклось ни одного токена) → 'approve':
 * не наказываем за отсутствие данных, downside мал, движок разрулит по CPO.
 */
export interface ClusterRelevanceSignals {
  /**
   * У кластера есть слово, выученное как НЕРЕЛЕВАНТНОЕ из прошлых решений менеджера по этому
   * товару (и это слово НЕ входит в базовый релевантный набор). Перебивает все прочие сигналы.
   */
  learnedNegative: boolean;
  /** По фразам кластера есть заказы (РК или JAM-органика) за окно. */
  hasOrders: boolean;
  /** Сколько стем-токенов фраз кластера пересеклось с релевантным набором товара. */
  matchedTokens: number;
  /** Всего извлечённых стем-токенов у фраз кластера (0 — нечего сопоставлять). */
  clusterTokens: number;
}

export type SuggestedReviewAction = "approve" | "blacklist";

export function suggestReviewAction(signals: ClusterRelevanceSignals): SuggestedReviewAction {
  // 0. Менеджер уже бракнул такое слово по этому товару — самый авторитетный сигнал,
  //    перебивает заказы (случай «для шиншилл»: заказы есть, но это чужой трафик).
  if (signals.learnedNegative) return "blacklist";
  // 1. Уже продаётся по кластеру — релевантен.
  if (signals.hasOrders) return "approve";
  // Серая зона: нечего сопоставлять (нет токенов) — пускаем, downside мал, движок разрулит.
  if (signals.clusterTokens === 0) return "approve";
  // 2. Фразы кластера пересекаются с профилем товара / одобренными / выученными positive.
  if (signals.matchedTokens > 0) return "approve";
  // 3. Ни заказов, ни пересечения — явный мусор.
  return "blacklist";
}
