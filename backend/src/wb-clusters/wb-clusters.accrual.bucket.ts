/**
 * Ценовая корзина кластера (price_bucket) для накопительных счётчиков.
 *
 * Статистика кластера привязана к ценовому уровню товара (цена — главный фактор конверсии).
 * Корзина = опорная цена ближайшего геометрического уровня с относительным шагом ±5%: цены
 * в пределах ~±2.5% попадают в одну корзину. Так колебания цены на копейки не плодят корзины,
 * а смена цены 1400→2000 переключает корзину и НЕ теряет ранее накопленные данные 1400-корзины.
 *
 * Чистая функция (без сети/БД) — переиспользуется и в аккумуляторе, и в движке решений.
 */

/** Относительный шаг корзины (5%). */
const PRICE_BUCKET_STEP = 0.05;

/**
 * Ключ ценовой корзины по цене со скидкой (целые ₽). Возвращает опорную цену уровня строкой
 * (напр. 1400₽ и 1420₽ → одна корзина "1429"). Цена ≤ 0 / не число → "0" (вырожденная корзина).
 */
export function priceBucket(price: number | null | undefined): string {
  if (price == null || !Number.isFinite(price) || price <= 0) return "0";
  const idx = Math.round(Math.log(price) / Math.log(1 + PRICE_BUCKET_STEP));
  const ref = Math.round(Math.pow(1 + PRICE_BUCKET_STEP, idx));
  return String(ref);
}
